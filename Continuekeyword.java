package Basicprograms;

public class Continuekeyword {

	public static void main(String[] args) {
     // Continue :- It is used to skip the particular ( iteration)'
		// Iteration means what ---> Repetition
		
		for (int i = 0;  i <10 ; i++)
			
		{
			
			if (i==2)
				
			{
				continue;
			}
			
			System.out.println(i);
		}

	}

}
